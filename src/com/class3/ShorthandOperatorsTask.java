package com.class3;

public class ShorthandOperatorsTask {
	
	public static void main(String[] args) {
		
		int num=20;
		num+=80;
	    System.out.println(num);
	    
	    int num1=97;
	    num1-=30;
	    System.out.println(num1);
	    
	    int cakePiece=11;
	    cakePiece/=4;
	    System.out.println(cakePiece);
	    
	    int cakePiece1=25;
	    cakePiece1%=7;
	    System.out.println(cakePiece1);
	    
	    
	    
	    
	    
	}
	

}
